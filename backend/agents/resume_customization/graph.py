def run_resume_customization(
    user_id: str,
    job_description: str,
    company_name: str,
    user_resume: str,
    user_profile: dict
) -> ResumeCustomizationState:
    """
    Run the complete resume customization workflow

    Args:
        user_id: User ID
        job_description: Job description text
        company_name: Company name
        user_resume: Original resume text
        user_profile: User profile dictionary

    Returns:
        Final state with customized resume and all metadata
    """
    print("\n" + "="*80)
    print("üöÄ RESUME CUSTOMIZATION WORKFLOW")
    print("="*80 + "\n")

    start_time = time.time()

    # Initialize state
    initial_state: ResumeCustomizationState = {
        "user_id": user_id,
        "job_description": job_description,
        "company_name": company_name,
        "user_resume": user_resume,
        "user_profile": user_profile,
        "jd_analysis": None,
        "parsed_resume": None,
        "github_repos": None,
        "matched_projects": None,
        "optimized_experience": None,
        "customized_resume": None,
        "ats_score": None,
        "ats_feedback": None,
        "retry_count": 0,
        "qa_results": None,
        "hallucination_check": None,
        "diff_report": None,
        "current_agent": None,
        "progress_messages": [],
        "errors": [],
        "execution_time": None
    }

    # Create and run graph
    graph = create_resume_customization_graph()

    print("üìã Running 9-agent workflow with optimized execution order...\n")

    # Execute workflow
    final_state = graph.invoke(initial_state)

    # Calculate execution time
    end_time = time.time()
    execution_time = end_time - start_time
    final_state["execution_time"] = execution_time

    print("\n" + "="*80)
    print(f"‚úÖ WORKFLOW COMPLETE ({execution_time:.2f}s)")
    print("="*80)

    # Print summary
    print(f"\nüìä SUMMARY:")
    print(f"  ‚Ä¢ ATS Score: {final_state.get('ats_score', 0):.1f}%")
    print(f"  ‚Ä¢ Projects Matched: {len(final_state.get('matched_projects', []))}")
    print(f"  ‚Ä¢ Experience Entries Optimized: {len(final_state.get('optimized_experience', []))}")
    print(f"  ‚Ä¢ Hallucination Check: {'‚úÖ PASSED' if final_state.get('hallucination_check') else '‚ö†Ô∏è REVIEW NEEDED'}")
    print(f"  ‚Ä¢ Errors: {len(final_state.get('errors', []))}")

    if final_state.get("errors"):
        print("\n‚ö†Ô∏è Errors encountered:")
        for error in final_state["errors"][:3]:
            print(f"  ‚Ä¢ {error}")

    return final_state
